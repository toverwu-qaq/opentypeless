pub fn style_override(id: &str) -> Option<AppStyleOverride> {
    use ArtifactKind::*;
    use Density::*;
    use Formality::*;
    use ListBehavior::*;
    use MarkupPolicy::*;

    let value = match id {
        "gmail" | "outlook" => AppStyleOverride {
            artifact_kind: Email,
            formality: Professional,
            density: Balanced,
            markup: PlainText,
            list_behavior: NumberWhenExplicit,
        },
        "slack" => AppStyleOverride {
            artifact_kind: Message,
            formality: Casual,
            density: Compact,
            markup: PlainText,
            list_behavior: LineBreaks,
        },
        "teams" | "lark" => AppStyleOverride {
            artifact_kind: Message,
            formality: Neutral,
            density: Compact,
            markup: PlainText,
            list_behavior: LineBreaks,
        },
        "linear" | "figma" => AppStyleOverride {
            artifact_kind: TaskUpdate,
            formality: Neutral,
            density: Compact,
            markup: Light,
            list_behavior: Preserve,
        },
        "jira" => AppStyleOverride {
            artifact_kind: TaskUpdate,
            formality: Professional,
            density: Balanced,
            markup: Light,
            list_behavior: NumberWhenExplicit,
        },
        "github" | "gitlab" => AppStyleOverride {
            artifact_kind: DeveloperNote,
            formality: Neutral,
            density: Compact,
            markup: Light,
            list_behavior: Preserve,
        },
        "chatgpt" | "claude" => AppStyleOverride {
            artifact_kind: Prompt,
            formality: Neutral,
            density: Balanced,
            markup: Structured,
            list_behavior: NumberWhenExplicit,
        },
        "zendesk" => AppStyleOverride {
            artifact_kind: SupportReply,
            formality: Professional,
            density: Balanced,
            markup: PlainText,
            list_behavior: NumberWhenExplicit,
        },
        "intercom" => AppStyleOverride {
            artifact_kind: SupportReply,
            formality: Neutral,
            density: Compact,
            markup: PlainText,
            list_behavior: LineBreaks,
        },
        "linkedin" => AppStyleOverride {
            artifact_kind: SocialPost,
            formality: Professional,
            density: Balanced,
            markup: PlainText,
            list_behavior: Preserve,
        },
        "x" => AppStyleOverride {
            artifact_kind: SocialPost,
            formality: Casual,
            density: Compact,
            markup: PlainText,
            list_behavior: Preserve,
        },
        _ => return None,
    };
    Some(value)
}
