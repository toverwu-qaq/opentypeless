#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum ReleaseTier {
    Reference,
    Extended,
    Fallback,
}

#[derive(Clone, Debug, PartialEq, Eq)]
pub struct TitleMarkerDefinition {
    pub suffix: String,
    pub required_host_suffix: Option<String>,
    pub required_native_identity: Option<String>,
}

impl TitleMarkerDefinition {
    fn for_host(suffix: &str, required_host_suffix: &str) -> Self {
        Self {
            suffix: suffix.to_string(),
            required_host_suffix: Some(required_host_suffix.to_string()),
            required_native_identity: None,
        }
    }
}

#[derive(Clone, Debug, PartialEq, Eq)]
pub struct ProfileDefinition {
    pub id: String,
    pub family: ContextFamily,
    pub app_label: String,
    pub icon_key: String,
    pub override_id: Option<String>,
    pub exact_hosts: Vec<String>,
    pub host_suffixes: Vec<String>,
    pub native_identities: Vec<String>,
    pub process_aliases: Vec<String>,
    pub title_markers: Vec<TitleMarkerDefinition>,
    pub release_tier: ReleaseTier,
}

impl ProfileDefinition {
    #[allow(clippy::too_many_arguments)]
    fn new(
        id: &str,
        family: ContextFamily,
        app_label: &str,
        icon_key: &str,
        override_id: Option<&str>,
        exact_hosts: &[&str],
        host_suffixes: &[&str],
        native_identities: &[&str],
        process_aliases: &[&str],
        release_tier: ReleaseTier,
    ) -> Self {
        Self {
            id: id.to_string(),
            family,
            app_label: app_label.to_string(),
            icon_key: icon_key.to_string(),
            override_id: override_id.map(str::to_string),
            exact_hosts: strings(exact_hosts),
            host_suffixes: strings(host_suffixes),
            native_identities: strings(native_identities),
            process_aliases: strings(process_aliases),
            title_markers: Vec::new(),
            release_tier,
        }
    }

    fn with_title_marker(mut self, marker: TitleMarkerDefinition) -> Self {
        self.title_markers.push(marker);
        self
    }
}

fn strings(values: &[&str]) -> Vec<String> {
    values.iter().map(|value| (*value).to_string()).collect()
}

#[allow(clippy::too_many_arguments)]
fn reference(
    id: &str,
    family: ContextFamily,
    label: &str,
    icon: &str,
    override_id: Option<&str>,
    exact_hosts: &[&str],
    host_suffixes: &[&str],
    native_ids: &[&str],
    process_aliases: &[&str],
) -> ProfileDefinition {
    ProfileDefinition::new(
        id,
        family,
        label,
        icon,
        override_id,
        exact_hosts,
        host_suffixes,
        native_ids,
        process_aliases,
        ReleaseTier::Reference,
    )
}

#[allow(clippy::too_many_arguments)]
fn extended(
    id: &str,
    family: ContextFamily,
    label: &str,
    icon: &str,
    exact_hosts: &[&str],
    host_suffixes: &[&str],
    native_ids: &[&str],
    process_aliases: &[&str],
) -> ProfileDefinition {
    ProfileDefinition::new(
        id,
        family,
        label,
        icon,
        None,
        exact_hosts,
        host_suffixes,
        native_ids,
        process_aliases,
        ReleaseTier::Extended,
    )
}

